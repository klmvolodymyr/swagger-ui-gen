<?php

namespace VolodymyrKlymniuk\SwaggerUIGen\Component\Model\Common;

trait NameRequireTrait
{
    /**
     * @var string
     */
    private $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}